1.¿ Que es un control de versiones?
Es un sistema que registra los cambios realizados sobre un 
archivo o conjunto de archivos a lo largo del tiempo, de modo 
que puedas recuperar versiones específicas más adelante.

2.¿Cuáles son las ventajas de utilizar un controlador de versiones?
- Puedes volver a cualquier punto del desarrollo para ver qué aspecto tenía 
un determinado fichero de código, o volver a una versión donde todo funcionaba 
antes de haber metido la pata.

- Puedes trabajar en distintas características de forma simultánea, guardando 
los cambios en cada una de ellas, y uniéndolos al desarrollo principal si ya 
han sido lo suficientemente probadas.

- Puedes echar un vistazo para ver quién realizó un determinado cambio, y cuándo lo hizo.

