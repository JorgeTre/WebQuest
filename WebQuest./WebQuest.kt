5.Investigue los siguientes términos Push, Commit, Branch, Delete, Stack y Repositorio.

Push: cuando alguien del equipo hace un cambio en el repositorio nos permite enviar los cambios al servidor.

Commit: añadir un cambio o un fichero a un repositorio debemos pasar esos cambios a stage antes de hacer los cambios (commit) y enviarlos (push)  al servidor. Realizar un cambio en el proyecto. Formalmente, almacenar un cambio en la base de datos del control de versiones de forma que pueda ser incorporado en lanzamientos futuros del proyecto. "Commit" puede ser utilizado como un verbo o como un sustantivo. Como un sustantivo, es esencialmente un sinónimo de "cambio".

Branch: Es una copia del proyecto, bajo el control de versiones, pero aislado, de forma que los cambios realizados en esta rama no afecten al resto del proyecto y vice versa, excepto cuando los cambios sean deliberadamente "unidos" de un lado al otro. Las ramas también son conocidas como "lineas de desarrollo".

Delete: elimina algunos ficheros de la lista de la configuración.

Stack: es un desarrollador con un perfil técnico muy completo que conoce bien tanto lo referente a back-end como lo referente a front-end, se maneja en sistemas y sabe entender. Se trata de un perfil cada vez más demandado y bien remunerado.

Repositorio: El repositorio es el lugar en el que se almacenan los datos actualizados e históricos de cambios, a menudo en un servidor. Una base de datos en la que los cambios son almacenados. Algunas versiones de sistemas de control de versiones son centralizados, es decir, existe un único repositorio maestro, el cual almacena todos los cambios en el proyecto.

